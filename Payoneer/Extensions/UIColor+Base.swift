import Foundation
import UIKit

extension UIColor {

    @nonobjc class var colorPrimary: UIColor {
        return UIColor(named: "primary")!
    }

    @nonobjc class var colorSecondary: UIColor {
        return UIColor(named: "secondary")!
    }

}
